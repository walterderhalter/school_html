<h2> Gesundheit</h2>

<pre style ="font-family:arial;">
Erdbeeren haben aufgrund ihres hohen Wassergehalts nur sehr wenig an Kalorien. 
Mit 62 mg/100 g enthalten sie mehr Vitamin C als Zitrusfrüchte. 
Besonders hoch sind auch der Folsäure-Gehalt sowie der Gehalt der Mineralstoffe Kalium, Eisen, Magnesium und Kalzium. 
Erdbeeren sind zudem reich an den antioxidativ wirkenden Polyphenolen.

