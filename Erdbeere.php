<div class="Geruch">
<h2> Geruch </h2>
<pre style ="font-family:arial;">
Verantwortlich dafür ist vor allem das Aroma. 
Denn nicht nur der reine Geschmack, den wir über die Zunge wahrnehmen, 
sondern auch der Duft ist entscheidend dafür, wie uns etwas schmeckt. 
Im Aroma der Gartenerdbeere sind über zehn verschiedene Geruchsstoffe enthalten. 
Vor allem einer aber, das Furaneol, ist charakteristisch.

In reifen Erdbeeren sind bis zu 50 Milligramm Furaneol pro Kilogramm enthalten, konnten die Lebensmittelchemiker zeigen. 
Diese Menge sei ausreichend, sodass der Mensch sie über die Nase wahrnehmen könne. 
Das Furaneol rieche in gewisser Weise leicht karamellartig, so die Wissenschaftler. 
Bisher war nicht bis in jedes Detail bekannt, wie Erdbeeren aus dem Fruchtzucker Furaneol herstellen – der letzte Schritt in der Reaktionskette war noch unklar. 
Die Wissenschaftler aus München konnten diesen letzten Schritt nun aber aufklären.

Quelle: <a href="https://www.welt.de/print/die_welt/wissen/article116751390/Der-Duft-von-Erdbeeren.html#:~:text=Im%20Aroma%20der%20Gartenerdbeere%20sind,enthalten%2C%20konnten%20die%20Lebensmittelchemiker%20zeigen." 
    target="_self">Link</a>
</pre>
