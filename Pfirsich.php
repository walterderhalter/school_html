		<h2>Geruch</h2>
		<pre style="font-family: arial">
Der süße und fruchtige Pfirsich (Prunus persica) mit seiner zarten Haut, 
seinen geschwungenen Rundungen und seinem köstlichen, saftigen Fruchtfleisch 
ließ die Menschen schon seit Urzeiten an die Freuden der Liebe denken.

Aus China – wo der Pfirsich ein Symbol der Unsterblichkeit ist – stammt der Pfirisch – dort 
begann man bereits im Jahr 6000 v. Chr. mit der Zucht, nachgewiesen ist dort auch seine Kultivierung 
seit 2000 v. Chr. Über Persien und Griechenland gelangte er durch die Römer auch nach Europa, wo wir ihn heute 
genießen und seinen fruchtigen und süßen Duft in oftmals feminin ausgerichteten Düften erleben dürfen.

