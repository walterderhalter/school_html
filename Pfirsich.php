	<div class="Gesundheit">
		<h2>Gesundheit</h2>

		<pre style="font-family: arial">
Hinter dem süßen Aroma der Pfirsiche steckt eine Vielzahl wertvoller Nährstoffe und Vitamine. 
Pfirsiche liefern unter anderem Kalzium, Kalium, Eisen und Magnesium sowie die Vitamine B1, B2, C und K. Auch Vitamin E ist enthalten.

Eine weitere gesunde Komponente sind die Flavonoide: pflanzliche Farbstoffe, 
die immunstärkend wirken und als Antioxidantien unseren Organismus vor freien Radikalen schützen. 
Ihre Zellschutzfunktion reduziert damit das Krebsrisiko. 
Zusätzlich beugen Flavonoide Herz-Kreislauf-Erkrankungen vor und senken den Cholesterinspiegel – damit beugen sie auch dem metabolischen Syndrom entgegen. 
Besonders weißfleischige Sorten liefern mit 28-111 mg pro Frucht reichlich gesunde Pflanzenstoffe. 
Dabei gilt: Je frischer das Obst ist, desto mehr gesunde Inhaltsstoffe sind enthalten.

