	<div class="Beliebtheit">
		<h3>Beliebtheit</h3>
		<p>Hier werden drei Gründe genannt, weshalb Pfiriche so toll sind.</p>

		<details>
			<summary>Grund 1: Wenige Kalorien, viel Vitamin B3</summary>
			<p>
				Pfirsiche und Nektarinen sind mit etwa 40 Kilokalorien auf 100 Gramm und
				einem Wassergehalt von circa 90 Prozent ein leichter Sommergenuss. <br>
				Pfirsiche enthalten neben Selen, Zink und Magnesium sehr viel Niacin
				(Vitamin B3). <br>
				Niacin brauchen unsere Muskeln und unsere Haut, vor allem aber unsere
				Nerven, um regenerieren zu können. <br>
				Außerdem bildet unser Körper mithilfe von Vitamin B3 Botenstoffe im Gehirn,
				die unsere Stimmung heben. <br>
				Pfirsiche beruhigen also unsere Nerven und sorgen für gute Laune. <br>
				Jede Menge Vitamin C stärkt zudem das Immunsystem. <br>
				Die reichlich vorhandenen Polyphenole haben ebenfalls eine positive Wirkung
				auf die Gesundheit - sie wirken entzündungshemmend <br>
				und senken das Risiko für Herz-Kreislauf-Erkrankungen.<br>
			</p>
		</details>

		<details>
			<summary>Grund 2: Nektarinen sind Kalium-Lieferanten</summary>
			<p>
				Nektarinen sind Pfirsich-Mutanten. Das heißt, sie sind nicht extra gezüchtet
				worden, sondern als Laune der Natur aus dem Pfirsich entstanden und wurden
				dann kultiviert. <br>
				Sie sind genauso gesund wie Pfirsiche - vor allem für unser Herz: Denn
				Nektarinen haben sehr viel Kalium, gut 200 Milligramm pro 100 Gramm
				Fruchtfleisch. <br>
				Das ist viel mehr als zum Beispiel Äpfel oder Birnen haben. Kalium steuert
				unsere Herzfunktion, denn als Elektrolyt regelt es zum Beispiel die
				Zellspannung. <br>
				Außerdem senkt Kalium den Blutdruck und verringert so das
				Schlaganfallrisiko.<br>
			</p>
		</details>

		<details>
			<summary>Grund 3: Pfirsich-Kernöl gut für die Haut</summary>
			<p>
				In der Kosmetik-Industrie ist Pfirsich-Kernöl sehr beliebt, denn es ist
				reich an Linol-Säure. <br>
				Diese kann Hautirritationen lindern und Entzündungen hemmen. Und das Öl
				enthält viel Oleinsäure: <br>
				Oleinsäure zählt zu den Omega-9-Fettsäuren und kann ebenfalls leichte
				Hautirritationen abmildern. <br>
				Vorsichtig sollte nur all jene sein, die zu sehr fettiger Haut neigen, hier
				kann Oleinsäure Hautprobleme verstärken.<br>
			</p>
		</details>
